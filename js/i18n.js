class MeowI18n {
    constructor() {
        this.lang = 'zh-CN';
        this.elements = document.querySelectorAll('[data-i18n], [data-i18n-title], [data-i18n-ph]');
    }

    async init() {
        const data = await new Promise(resolve => chrome.storage.sync.get(['meow_locale'], resolve));
        if (data.meow_locale) {
            this.lang = data.meow_locale;
        } else {
            const navLang = navigator.language;
            if (navLang.startsWith('zh-TW') || navLang.startsWith('zh-HK')) this.lang = 'zh-TW';
            else if (navLang.startsWith('en')) this.lang = 'en';
            else this.lang = 'zh-CN';
        }
        // Try-catch mainly for content scripts which might run before DOM is fully ready or in different context
        try {
            this.updatePage();
            this.updatePlaceholders();
        } catch(e) {}
        
        // Listen for storage changes to update language dynamically across windows
        chrome.storage.onChanged.addListener((changes, area) => {
            if (area === 'sync' && changes.meow_locale) {
                this.lang = changes.meow_locale.newValue;
                this.updatePage();
                this.updatePlaceholders();
            }
        });
        
        const select = document.getElementById('language-select');
        if (select) select.value = this.lang;
    }

    async setLang(lang) {
        this.lang = lang;
        await chrome.storage.sync.set({ 'meow_locale': lang });
        this.updatePage();
        this.updatePlaceholders();
    }

    t(key, params = {}) {
        const dict = translations[this.lang] || translations['zh-CN'];
        let str = dict[key] || key;
        Object.keys(params).forEach(k => {
            str = str.replace(`{${k}}`, params[k]);
        });
        return str;
    }

    updatePage() {
        document.documentElement.lang = this.lang;
        this.elements = document.querySelectorAll('[data-i18n], [data-i18n-title], [data-i18n-ph]');
        this.elements.forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (key) el.innerHTML = this.t(key);

            const titleKey = el.getAttribute('data-i18n-title');
            if (titleKey) el.setAttribute('title', this.t(titleKey));

            const phKey = el.getAttribute('data-i18n-ph');
            if (phKey) el.setAttribute('placeholder', this.t(phKey));
        });
        
        document.querySelectorAll('option[data-i18n]').forEach(opt => {
             const key = opt.getAttribute('data-i18n');
             if (key) opt.textContent = this.t(key);
        });
        
        const toggleBtn = document.getElementById('toggle-sidebar-btn');
        if (toggleBtn) toggleBtn.setAttribute('title', this.t('toggle_sidebar_title'));
        
        const toggleRightBtn = document.getElementById('toggle-right-btn');
        if (toggleRightBtn) toggleRightBtn.setAttribute('title', this.t('toggle_right_title'));
    }

    updatePlaceholders() {
        const map = {
            'new-task-input': 'todo_placeholder',
            'fin-amount': 'finance_placeholder_amount',
            'fin-note': 'finance_placeholder_note',
            'pe-name': 'pre_placeholder_name',
            'cd-title-input': 'cd_placeholder_title',
            'new-habit-input': 'habit_placeholder',
            'alarm-label-input': 'alarm_placeholder',
            'scratchpad-area': 'sp_placeholder',
            'webdav-url': 'webdav_server_url',
            'webdav-user': 'webdav_username',
            'webdav-pass': 'webdav_password',
            'popup-exchange-amount': 'exchange_ph_amount',
            'popup-exchange-result': 'exchange_ph_result',
            'exchange-amount': 'exchange_ph_amount',
            'exchange-result-val': 'exchange_ph_result',
            'fuel-region-input': 'fuel_ph_region'
        };

        for (const [id, key] of Object.entries(map)) {
            const el = document.getElementById(id);
            if (el) el.placeholder = this.t(key);
        }
    }
}

window.meowI18n = new MeowI18n();